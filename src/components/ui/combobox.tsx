interface ComboboxOpenContextValue {
  open: boolean
  setOpen: (open: boolean, eventDetails: ComboboxPrimitive.Root.ChangeEventDetails) => void
}

const ComboboxOpenContext = createContext<ComboboxOpenContextValue>({
  open: false,
  setOpen: () => {},
})

function Combobox<Value, Multiple extends boolean | undefined = false>({
  open: openProp,
  defaultOpen = false,
  onOpenChange,
  modal,
  ...props
}: ComboboxPrimitive.Root.Props<Value, Multiple>) {
  const isDesktop = useMediaQuery('(min-width: 768px)')
  const isControlled = openProp !== undefined
  const [uncontrolledOpen, setUncontrolledOpen] = useState(defaultOpen)
  const open = isControlled ? openProp : uncontrolledOpen

  const setOpen = useCallback(
    (next: boolean, eventDetails: ComboboxPrimitive.Root.ChangeEventDetails) => {
      if (!isControlled) setUncontrolledOpen(next)
      onOpenChange?.(next, eventDetails)
    },
    [isControlled, onOpenChange],
  )

  const contextValue = useMemo(() => ({ open, setOpen }), [open, setOpen])

  // On mobile the list lives in our Sheet (a modal Dialog), so let the Sheet own
  // scroll-lock/focus-trap and keep base-ui non-modal to avoid a double lock.
  const resolvedModal = modal ?? (isDesktop ? undefined : false)

  return (
    <ComboboxOpenContext.Provider value={contextValue}>
      <ComboboxPrimitive.Root open={open} onOpenChange={setOpen} modal={resolvedModal} {...props} />
    </ComboboxOpenContext.Provider>
  )
}

function ComboboxValue({ ...props }: ComboboxPrimitive.Value.Props) {
  return <ComboboxPrimitive.Value data-slot="combobox-value" {...props} />
}

function ComboboxTrigger({ className, children, ...props }: ComboboxPrimitive.Trigger.Props) {
  return (
    <ComboboxPrimitive.Trigger
      data-slot="combobox-trigger"
      className={cn("[&_svg:not([class*='size-'])]:size-4", className)}
      {...props}
    >
      {children}
      <ChevronDownIcon className="text-muted-foreground pointer-events-none size-4" />
    </ComboboxPrimitive.Trigger>
  )
}

function ComboboxClear({ className, ...props }: ComboboxPrimitive.Clear.Props) {
  return (
    <ComboboxPrimitive.Clear
      data-slot="combobox-clear"
      render={<InputGroupButton variant="ghost" size="icon-xs" />}
      className={cn(className)}
      {...props}
    >
      <XIcon className="pointer-events-none" />
    </ComboboxPrimitive.Clear>
  )
}

function ComboboxInput({
  className,
  children,
  disabled = false,
  showTrigger = true,
  showClear = false,
  ...props
}: ComboboxPrimitive.Input.Props & {
  showTrigger?: boolean
  showClear?: boolean
}) {
  return (
    <InputGroup className={cn('w-auto', className)}>
      <ComboboxPrimitive.Input render={<InputGroupInput disabled={disabled} />} {...props} />
      <InputGroupAddon align="inline-end">
        {showTrigger && (
          <InputGroupButton
            size="icon-xs"
            variant="ghost"
            render={<ComboboxTrigger />}
            data-slot="input-group-button"
            className="group-has-data-[slot=combobox-clear]/input-group:hidden data-pressed:bg-transparent"
            disabled={disabled}
          />
        )}
        {showClear && <ComboboxClear disabled={disabled} />}
      </InputGroupAddon>
      {children}
    </InputGroup>
  )
}

function ComboboxContent({
  className,
  side = 'bottom',
  sideOffset = 6,
  align = 'start',
  alignOffset = 0,
  anchor,
  children,
  title,
  ...props
}: ComboboxPrimitive.Popup.Props &
  Pick<
    ComboboxPrimitive.Positioner.Props,
    'side' | 'align' | 'sideOffset' | 'alignOffset' | 'anchor'
  > & {
    title?: string
  }) {
  const isDesktop = useMediaQuery('(min-width: 768px)')
  const { open, setOpen } = useContext(ComboboxOpenContext)

  if (!isDesktop) {
    return (
      <Sheet
        open={open}
        onOpenChange={(next, eventDetails) =>
          setOpen(next, eventDetails as ComboboxPrimitive.Root.ChangeEventDetails)
        }
      >
        <SheetContent
          side="bottom"
          showCloseButton={false}
          data-slot="combobox-content"
          className={cn(
            'bg-popover text-popover-foreground max-h-[75vh] gap-0 overflow-y-auto rounded-t-3xl pt-2 pb-[max(1rem,env(safe-area-inset-bottom))]',
            className,
          )}
        >
          <div className="flex items-center gap-3 px-3 pb-2">
            <SheetTitle className={title ? 'text-muted-foreground text-sm font-medium' : 'sr-only'}>
              {title ?? 'Options'}
            </SheetTitle>
            <SheetClose
              render={
                <Button variant="ghost" size="icon-sm" aria-label="Close" className="ml-auto" />
              }
            >
              <XIcon />
            </SheetClose>
          </div>
          {children}
        </SheetContent>
      </Sheet>
    )
  }

  return (
    <ComboboxPrimitive.Portal>
      <ComboboxPrimitive.Positioner
        side={side}
        sideOffset={sideOffset}
        align={align}
        alignOffset={alignOffset}
        anchor={anchor}
        className="isolate z-50"
      >
        <ComboboxPrimitive.Popup
          data-slot="combobox-content"
          data-chips={!!anchor}
          className={cn(
            'group/combobox-content rounded-card bg-popover text-popover-foreground shadow-popover ring-foreground/10 data-[side=bottom]:slide-in-from-top-2 data-[side=inline-end]:slide-in-from-left-2 data-[side=inline-start]:slide-in-from-right-2 data-[side=left]:slide-in-from-right-2 data-[side=right]:slide-in-from-left-2 data-[side=top]:slide-in-from-bottom-2 *:data-[slot=input-group]:border-input/30 *:data-[slot=input-group]:bg-input/30 data-open:animate-in data-open:fade-in-0 data-open:zoom-in-95 data-closed:animate-out data-closed:fade-out-0 data-closed:zoom-out-95 relative max-h-(--available-height) w-(--anchor-width) max-w-(--available-width) min-w-[calc(var(--anchor-width)+--spacing(7))] origin-(--transform-origin) overflow-hidden ring-1 duration-100 data-[chips=true]:min-w-(--anchor-width) *:data-[slot=input-group]:m-1 *:data-[slot=input-group]:mb-0 *:data-[slot=input-group]:h-8 *:data-[slot=input-group]:shadow-none',
            className,
          )}
          {...props}
        >
          {children}
        </ComboboxPrimitive.Popup>
      </ComboboxPrimitive.Positioner>
    </ComboboxPrimitive.Portal>
  )
}

